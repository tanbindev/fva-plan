package com.cn.hansontable.domain;

public class PNO {
	String PNO;

	String STATUS;

	String EX_PNO;
	/**
	 * @return the pNO
	 */
	public String getPNO() {
		return PNO;
	}

	/**
	 * @param pNO the pNO to set
	 */
	public void setPNO(String pNO) {
		PNO = pNO;
	}

	public String getSTATUS() {
		return STATUS;
	}

	public void setSTATUS(String STATUS) {
		this.STATUS = STATUS;
	}

	public String getEX_PNO() {
		return EX_PNO;
	}

	public void setEX_PNO(String EX_PNO) {
		this.EX_PNO = EX_PNO;
	}
	
}
